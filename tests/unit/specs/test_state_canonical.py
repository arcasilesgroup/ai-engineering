"""CI guard for spec-125 D-125-09: canonical state/ structure (binary contract).

`.ai-engineering/state/` MUST contain only canonical entries. Per spec-125
the optional category was eliminated: every artifact is canonical, a
documented transient (SQLite WAL siblings, gate-findings), or forbidden.

Canonical files (REQUIRED_FILES)
--------------------------------
- ``state.db``                    -- unified SQLite projection (spec-122-b D-122-06)
- ``framework-events.ndjson``     -- immutable Article-III source-of-truth
- ``observation-events.ndjson``-- instinct learning log
- ``hooks-manifest.json``         -- sha256-pinned hook integrity ledger

Canonical directories (REQUIRED_DIRS)
-------------------------------------
- ``locks/`` -- file-lock coordination (POSIX *.lock files only)

Documented transient artifacts (DOCUMENTED_TRANSIENT_FILES + SQLITE_WAL_SIBLINGS)
---------------------------------------------------------------------------------
- ``state.db-shm`` / ``state.db-wal`` -- SQLite WAL-mode siblings (inevitable
  side-effects when state.db is opened; SQLite manages lifecycle).
- ``gate-findings.json`` -- regenerated by every ``ai-commit`` gate run,
  consumed by ``risk`` subcommands; gitignored, path-pinned by 14+ tests
  + ``risk_cmd.py`` UX. Same posture as SQLite WAL siblings: documented
  load-bearing infrastructure, not a canonical state artifact.

Forbidden entries (FORBIDDEN_FILES, FORBIDDEN_DIRS)
---------------------------------------------------
Spec-125 deletes the JSON fallbacks migrated to ``state.db`` plus the
relocated subdirs (``runtime/`` → ``.ai-engineering/runtime/``,
``gate-cache/`` → ``.ai-engineering/cache/gate/``). ``archive/`` and
``audit-archive/`` are deleted outright.
"""

from __future__ import annotations

from pathlib import Path

PROJECT_ROOT = Path(__file__).resolve().parents[3]
STATE_DIR = PROJECT_ROOT / ".ai-engineering" / "state"

# Files that MUST be present (canonical surface).
REQUIRED_FILES = (
    "framework-events.ndjson",
    "hooks-manifest.json",
    "observation-events.ndjson",
    "state.db",
)

# Directories that MUST be present.
REQUIRED_DIRS = ("locks",)

# SQLite WAL-mode sibling files. Inevitable when ``state.db`` is opened
# in WAL mode; SQLite manages their lifecycle. Documented as
# infrastructure rather than canonical artifacts.
SQLITE_WAL_SIBLINGS = frozenset({"state.db-shm", "state.db-wal"})

# gate-findings.json is regenerated by ``ai-commit`` after every gate run,
# gitignored, consumed by ``risk`` subcommands. Documented transient
# artifact (same posture as SQLite WAL), not a canonical state artifact.
# Path pinned by 14+ tests + risk_cmd.py UX.
#
# strategic-compact.json is the strategic-compact hook output (session-scoped
# day-bucket counter). Gitignored per spec-125 D-125-09 comment. Documented
# transient artifact — written on PreCompact / PostCompact hook events.
DOCUMENTED_TRANSIENT_FILES = frozenset({"gate-findings.json", "strategic-compact.json"})

# Files that MUST NOT be present (deprecated, deleted in spec-125).
FORBIDDEN_FILES = (
    "decision-store.json",
    "ownership-map.json",
    "install-state.json",
    "framework-capabilities.json",
    ".DS_Store",
)

# Directories that MUST NOT be present (relocated or deleted in spec-125).
FORBIDDEN_DIRS = (
    "archive",
    "audit-archive",
    "gate-cache",
    "runtime",
)


def test_state_directory_exists() -> None:
    assert STATE_DIR.is_dir(), (
        f"Canonical state directory missing at {STATE_DIR}. "
        "Per spec-122-b + spec-125 D-125-09 the projection lives here."
    )


def test_required_files_present() -> None:
    """Every canonical file is present (state.db plus the audit ledgers)."""
    missing = [name for name in REQUIRED_FILES if not (STATE_DIR / name).is_file()]
    assert not missing, (
        f"Required state files missing from {STATE_DIR}: {missing}. "
        "These define the canonical state surface (spec-125 D-125-09)."
    )


def test_required_dirs_present() -> None:
    """Every canonical directory is present."""
    missing = [name for name in REQUIRED_DIRS if not (STATE_DIR / name).is_dir()]
    assert not missing, (
        f"Required state directories missing from {STATE_DIR}: {missing}. "
        "These define the canonical layout per spec-125 D-125-09."
    )


def test_forbidden_files_absent() -> None:
    """The deprecated JSON fallbacks must not be present.

    Reappearance indicates either (a) a regression in the spec-125 cleanup,
    (b) a manual write that bypasses ``state.db``, or (c) a missing
    ``.gitignore`` entry. State.db is canonical; the JSON projections
    were migrated in spec-122-b/spec-123 and deleted in spec-125.
    """
    present = [name for name in FORBIDDEN_FILES if (STATE_DIR / name).exists()]
    assert not present, (
        f"Deprecated JSON fallbacks still present in {STATE_DIR}: {present}. "
        "spec-125 D-125-09 deleted these; their reappearance breaks the canonical "
        "contract. Investigate the writer that recreated them. "
        "Source-of-truth is state.db (decisions, ownership_map, etc.)."
    )


def test_forbidden_dirs_absent() -> None:
    """No FORBIDDEN_DIRS may exist under .ai-engineering/state/.

    These subdirs were relocated (``runtime/`` → ``.ai-engineering/runtime/``,
    ``gate-cache/`` → ``.ai-engineering/cache/gate/``) or deleted outright
    (``archive/``, ``audit-archive/``) in spec-125. Reappearance signals
    a writer that still references the old path.
    """
    for forbidden in FORBIDDEN_DIRS:
        assert not (STATE_DIR / forbidden).exists(), f"{forbidden}/ resurrected — see spec-125"


def test_locks_dir_pattern() -> None:
    """All entries under locks/ must be *.lock files (POSIX file-lock files).

    Anything else signals cruft accumulation. We use a pattern check
    instead of an allowlist so new locks don't require updating this test.
    """
    locks_dir = STATE_DIR / "locks"
    if locks_dir.exists():
        for entry in locks_dir.iterdir():
            assert entry.is_file() and entry.name.endswith(".lock"), f"non-lock entry: {entry.name}"


def test_no_unexpected_top_level() -> None:
    """Every file at the top of state/ is canonical or documented-transient.

    Binary contract per spec-125 D-125-09: no ``ALLOWED_OPTIONAL_FILES``.
    A novel filename signals a new artifact that needs an explicit
    decision: add to ``REQUIRED_FILES`` (canonical), document as
    transient infrastructure (``SQLITE_WAL_SIBLINGS`` /
    ``DOCUMENTED_TRANSIENT_FILES``), or migrate it into ``state.db``.
    """
    if not STATE_DIR.is_dir():
        return
    allowed = set(REQUIRED_FILES) | SQLITE_WAL_SIBLINGS | DOCUMENTED_TRANSIENT_FILES
    unexpected: list[str] = []
    for entry in sorted(STATE_DIR.iterdir()):
        if entry.is_dir():
            # Directories validated by test_required_dirs_present /
            # test_forbidden_dirs_absent.
            continue
        if entry.name in allowed:
            continue
        unexpected.append(entry.name)
    assert not unexpected, (
        f"Unexpected files at top of {STATE_DIR}: {unexpected}. "
        "Either add to REQUIRED_FILES (canonical), document as transient "
        "infrastructure (SQLITE_WAL_SIBLINGS / DOCUMENTED_TRANSIENT_FILES), "
        "or migrate the data into state.db. spec-125 D-125-09 forbids "
        "accidental new JSON fallbacks."
    )
